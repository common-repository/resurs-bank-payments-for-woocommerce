declare(strict_types=1);

namespace Resursbank\Ecom\Lib\Repository\Traits;

use JsonException;
use ReflectionException;
use Resursbank\Ecom\Config;
use Resursbank\Ecom\Exception\ApiException;
use Resursbank\Ecom\Exception\AttributeCombinationException;
use Resursbank\Ecom\Exception\AuthException;
use Resursbank\Ecom\Exception\ConfigException;
use Resursbank\Ecom\Exception\CurlException;
use Resursbank\Ecom\Exception\Validation\EmptyValueException;
use Resursbank\Ecom\Exception\Validation\IllegalTypeException;
use Resursbank\Ecom\Exception\Validation\IllegalValueException;
use Resursbank\Ecom\Exception\Validation\NotJsonEncodedException;
use Resursbank\Ecom\Exception\ValidationException;
use Resursbank\Ecom\Lib\Repository\Api\Mapi\GenerateToken;

/**
 * Helper class to validate JWT credentials.
 */
class ValidateJwtCredentials
{
    /**
     * @throws ApiException
     * @throws AuthException
     * @throws ConfigException
     * @throws CurlException
     * @throws EmptyValueException
     * @throws IllegalTypeException
     * @throws JsonException
     * @throws ReflectionException
     * @throws ValidationException
     * @throws AttributeCombinationException
     * @throws IllegalValueException
     * @throws NotJsonEncodedException
     */
    public static function exec(): bool
    {
        $auth = Config::getJwtAuth();

        if ($auth === null) {
            throw new ConfigException(message: 'JWT auth is not configured.');
        }

        (new GenerateToken(auth: $auth))->call();

        return true;
    }
}
